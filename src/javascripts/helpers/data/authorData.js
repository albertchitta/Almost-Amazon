// FILTER FAVORITE AUTHORS
const favoriteAuthors = () => new Promise((resolve, reject) => {
  axios.get(`${dbUrl}/authors.json?orderBy="favorite"&equalTo=true`)
    .then((response) => resolve(Object.values(response.data)))
    .catch(reject);
});

export {
  getAuthors,
  createAuthor,
  favoriteAuthors,
  deleteAuthor,
  getSingleAuthor,
  updateAuthor
};
